# ################################################################################################################################

if 0:
    from zato.common.typing_ import any_

# ################################################################################################################################
# ################################################################################################################################

def get_bytes_to_str_encoding():
    return 'utf8'

# ################################################################################################################################

def drop_sio_elems(elems:'any_', *to_drop:'any_') -> 'any_':
    out = list(set(elems))
    for out_elem in out:
        out_name = out_elem.name if isinstance(out_elem, Elem) else out_elem
        for drop_elem in to_drop:
            if out_name == drop_elem:
                out.remove(out_elem)
    return out

