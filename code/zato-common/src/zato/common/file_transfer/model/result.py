# stdlib
from dataclasses import dataclass

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class ActionResult:
    is_ok: 'bool' = True
    error: 'str' = ''

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class DeliveryResult:
    is_ok: 'bool' = True
    error: 'str' = ''
    checksum: 'str' = ''

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class DedupResult:
    is_ok: 'bool' = True
    error: 'str' = ''
    existing_txn_id: 'str' = ''

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class ChecksumResult:
    is_ok: 'bool' = True
    error: 'str' = ''
    checksum: 'str' = ''

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class PreprocessResult:
    is_ok: 'bool' = True
    errors: 'list' = None
    checksum: 'str' = ''

    def __post_init__(self):
        if self.errors is None:
            self.errors = []

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class VerifyResult:
    is_ok: 'bool' = True
    signer: 'str' = ''

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class KeyPairResult:
    public_key: 'any' = None
    private_key: 'any' = None

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class SearchResult:
    items: 'list' = None
    total: 'int' = 0

    def __post_init__(self):
        if self.items is None:
            self.items = []

# ################################################################################################################################
# ################################################################################################################################

@dataclass
class ExtractionResult:
    sender: 'str' = ''
    receiver: 'str' = ''
    document_id: 'str' = ''
    conversation_id: 'str' = ''
    group_id: 'str' = ''
    user_status: 'str' = ''
    custom_attrs: 'dict' = None
    errors: 'list' = None

    def __post_init__(self):
        if self.custom_attrs is None:
            self.custom_attrs = {}
        if self.errors is None:
            self.errors = []

