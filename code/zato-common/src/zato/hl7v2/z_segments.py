# Z-segments are locally defined by vendors and sites - HL7 reserves the whole
# Z<letter><letter> namespace and never specifies any of them, so the classes
# below carry no field descriptors. Values are read and assigned positionally
# (e.g. zds_1), which HL7Segment supports natively, and serialization reproduces
# the assigned positions verbatim. Every possible ID is defined upfront so that
# any real-world Z-segment can always be built from Python code. Where the
# de-facto meaning of a segment is known, the docstring records its provenance -
# documentation only, never behavior.

