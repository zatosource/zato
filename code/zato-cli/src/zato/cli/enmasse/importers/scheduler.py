class SchedulerImporter:

    @staticmethod
    def preprocess(items:'anylist') -> 'anylist':

        out = []

        for item in items:
            item = preprocess_item(item)

            if 'extra' in item:
                extra = item['extra']
                if isinstance(extra, list):
                    item['extra'] = '\n'.join(str(elem) for elem in extra if elem)

            out.append(item)

        return out

