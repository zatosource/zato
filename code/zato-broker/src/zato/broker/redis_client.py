class BrokerClient:

    def __init__(self, *, server:'ParallelServer | None'=None, **kwargs:'any_') -> 'None':
        self.server = server

    def publish(self, msg:'any_', *args:'any_', **kwargs:'any_') -> 'None':
        handle_broker_msg(msg, self.server.worker_store)

    invoke_async = publish

