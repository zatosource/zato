# stdlib
import json

# ################################################################################################################################
# ################################################################################################################################

if 0:
    from zato.common.typing_ import any_, anydict

# ################################################################################################################################
# ################################################################################################################################

def post_json(client:'any_', path:'str', body:'anydict') -> 'any_':
    """ Posts one JSON body to one endpoint.
    """
    data = json.dumps(body)

    out = client.post(path, data=data, content_type='application/json')
    return out

