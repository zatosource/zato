        r.udiCarrier = {'value': 'test'}
        template = NarrativeTemplate(
            fields=['udiCarrier'],
            labels={'udiCarrier': 'Custom Label'},
        )
        narrative = generate_narrative(r, template=template)
        root = parse_xhtml(narrative['div'])
        assert root.tag == XHTML_NS + 'div'
